from math import *

num = int(input("Enter a number: "))

squareroot = sqrt(num)
print(f"The square root of {num} is {squareroot}")

natural_log = log(num)
print(f"The natural logarithm of {num} is {natural_log}")

sine_value = sin(num)
print(f"The sine of {num} is {sine_value}")

print("Done")